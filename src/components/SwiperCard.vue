<template>
    <div>
        <img :src="productImages[this.currentSrc]"  class="h-80 w-96"/>  
        <button @click="leftArrow()" class="absolute top-0 my-36"><i class="fa fa-arrow-circle-left text-3xl bg-white w-10 rounded-full" aria-hidden="true"></i></button>
        <button @click="rightArrow()" class="absolute top-0 right-0 my-36"><i class="fa fa-arrow-circle-right text-3xl bg-white w-10 rounded-full" aria-hidden="true"></i></button>

    </div>

</template>
<script >

export default({
    data(){
        return{
            currentSrc: 0,
            increament: 0,
        }
    },
    props:{
       productImages: {},
    },
    methods:{
     leftArrow(){
          (this.currentSrc <= this.productImages.length - 1) ? (this.currentSrc==0? this.currentSrc = this.productImages.length-1 : this.currentSrc--) : this.currentSrc = this.productImages.length-2;
      },
      rightArrow(){
          (this.currentSrc <= this.productImages.length - this.currentSrc) ? this.currentSrc++ : this.currentSrc = 0;
      }
    }
})
</script>
